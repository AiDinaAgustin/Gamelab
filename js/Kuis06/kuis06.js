function hideEl() {
    // Sembunyikan elemen h3
    $("h3").hide();
  }
  
  function hideClass() {
    // Sembunyikan semua elemen dengan class "dengan-class"
    $(".dengan-class").hide();
  }
  
  function hideAttr() {
    // Sembunyikan semua elemen dengan atribut href
    $("a[href]").hide();
  }
  